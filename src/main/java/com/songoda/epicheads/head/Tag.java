package com.songoda.epicheads.head;

public class Tag {

    private final String name;
    private int count;

    public Tag(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
